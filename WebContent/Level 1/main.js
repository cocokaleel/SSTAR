function showImage() {
    var x = document.createElement("IMG");
    x.setAttribute("src", "Level1.jpg");
    x.setAttribute("width", "304");
    x.setAttribute("height", "228");
    x.setAttribute("alt", "Level1");
    document.body.appendChild(x);
}